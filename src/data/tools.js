// Build the mindmap tree structure
export function buildMindmapData() {
  const categoryMap = {
    hand_tool: { name: 'Hand Tools', children: {} },
    power_tool: { name: 'Power Tools', children: {} },
    specialized: { name: 'Specialized Tools', children: {} },
    measuring: { name: 'Measuring & Marking', children: {} },
    fastener: { name: 'Fasteners & Hardware', children: {} },
    adhesive: { name: 'Adhesives & Sealants', children: {} },
  }

  TOOLS.forEach((tool) => {
    const cat = categoryMap[tool.category]
    if (!cat) return
    if (!cat.children[tool.subcategory]) {
      cat.children[tool.subcategory] = { name: tool.subcategory, tools: [] }
    }
    cat.children[tool.subcategory].tools.push(tool)
  })

  return {
    name: 'All Tools',
    children: Object.values(categoryMap).map((cat) => ({
      name: cat.name,
      children: Object.values(cat.children).map((sub) => ({
        name: sub.name,
        children: sub.tools.map((t) => ({ name: t.name, id: t.id, emoji: t.emoji })),
      })),
    })),
  }
}
